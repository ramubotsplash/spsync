        private ObservableCollection<SyncLibraryItem> _allLibraries = new ObservableCollection<SyncLibraryItem>();
        public ObservableCollection<SyncLibraryItem> AllLibraries
        {
            get { return this._allLibraries; }
        }

        public static IEnumerable<SyncLibraryItem> GetLibraries(string uniqueId)
        {
            if (!uniqueId.Equals("AllGroups")) throw new ArgumentException("Only 'AllLibraries' is supported as a collection of groups");
            
            return _syncDataSource.AllLibraries;
        }

        public static SyncLibraryItem GetGroup(string uniqueId)
        {
            // Simple linear search is acceptable for small data sets
            var matches = _syncDataSource.AllLibraries.Where((group) => group.UniqueId.Equals(uniqueId));
            if (matches.Count() == 1) return matches.First();
            return null;
        }

        public static SyncDocumentItem GetDocumentItem(string uniqueId)
        {
            // Simple linear search is acceptable for small data sets
            var matches = _syncDataSource.AllLibraries.SelectMany(group => group.Items).Where((item) => item.UniqueId.Equals(uniqueId));
            if (matches.Count() == 1) return matches.First();
            return null;
        }

